


public class Pair_Utility_Probability {
	String utility;
	String probability;
	
	public Pair_Utility_Probability(String utility, String probability)
	{
		this.utility = utility;
		this.probability = probability;
	}
	
	public String getUtility()
	{
		return this.utility;
	}
	
	public String getProbability()
	{
		return this.probability;
	}
}
